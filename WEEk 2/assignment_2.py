# Creating an empty list
my_list = []

# Appending the elements to my list
my_list.append(10)
my_list.append(20)
my_list.append(30)
my_list.append(40)

# Inserting the value 15 at the second position
my_list.insert(1, 15)

# Extending the list with another list [50, 60, 70]
my_list.extend([50, 60, 70])

# Removing the last element from my list
my_list.pop()

# Sorting my list in ascending order
my_list.sort()

# Findig and printing the index of the value 30 in my_list
print(my_list.index(30))
